const os = require('os');


// check the platform 
console.log(os.platform());

// CPU Arch 
console.log(os.arch());

//Cpu core info 
console.log(os.cpus());

// Free memory 
console.log(os.freemem());

// Total Memory 

console.log(os.totalmem());

// Check home dir 
console.log(os.homedir());

// Up time 

console.log(os.uptime());
